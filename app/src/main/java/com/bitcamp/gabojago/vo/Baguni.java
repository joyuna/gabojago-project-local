package com.bitcamp.gabojago.vo;

public class Baguni {
  String id;
  
  int exno;
  
  int cnt;
  
  boolean act;

  public String getId() {
    return id;
  }

  public void setId(String id) {
    this.id = id;
  }

  public int getExno() {
    return exno;
  }

  public void setExno(int exno) {
    this.exno = exno;
  }

  public int getCnt() {
    return cnt;
  }

  public void setCnt(int cnt) {
    this.cnt = cnt;
  }

  public boolean isAct() {
    return act;
  }

  public void setAct(boolean act) {
    this.act = act;
  }
}
